# Este ejercicio trata sobre mis pizzas favoritas 

pizzas = ['pepperoni', 'hawaiiana', 'champiñones']

# Primero imprimo los nombres de las pizzas
for pizza in pizzas:
    print(pizza)

print()  # Dejo un espacio para separar las salidas

# Ahora imprimo frases diciendo que me gustan esas pizzas
for pizza in pizzas:
    print(f"Me gusta mucho la pizza de {pizza}.")

# Mensaje final fuera del bucle
print("\n¡La pizza es de mis comidas favoritas!")
